// Compressed data dedicated for dali clock digit font segments.
//
// It uses 20 bits/line instead of 32.
// Pack eight 5-bit numbers into four bytes:
// aaaaa bbbbb ccccc ddddd   phase 0
// eeeee fffff ggggg hhhhh   phase 1
//
// 765 43210 (bit 0..7)
// ---------
// ccc aaaaa (byte 0)
// ccd bbbbb (byte 1)
// ddd eeeee (byte 2)
// dhh fffff (byte 3)
// hhh ggggg (byte 4)
#define DALI_SEG_LINE(a, b, c, d, e, f, g, h) \
  ((c >> 2) << 5) | a, ((c & 3) << 6) | ((d >> 4) << 5) | b, \
  (((d >> 1) & 7) << 5) | e, ((d & 1) << 7) | ((h >> 3) << 5) | f, \
  ((h & 7) << 5) | g

